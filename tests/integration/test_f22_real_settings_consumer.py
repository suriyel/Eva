@contextmanager
def _env_override(**vars_: str) -> Iterator[None]:
    prev: dict[str, str | None] = {k: os.environ.get(k) for k in vars_}
    os.environ.update(vars_)
    try:
        yield
    finally:
        for k, v in prev.items():
            if v is None:
                os.environ.pop(k, None)
            else:
                os.environ[k] = v


def _git_init(workdir: Path) -> None:
    subprocess.run(["git", "init", "-q"], cwd=workdir, check=True)
    subprocess.run(
        [
            "git",
            "-c",
            "user.email=a@b",
            "-c",
            "user.name=A",
            "commit",
            "--allow-empty",
            "-q",
            "-m",
            "initial",
        ],
        cwd=workdir,
        check=True,
    )


def _git_commit_text_and_binary(workdir: Path) -> str:
    """Add a text file and a tiny PNG, commit, return SHA."""
    _git_init(workdir)
    (workdir / "README.md").write_text("hello\nworld\n", encoding="utf-8")
    # 8-byte PNG signature + 1-byte payload — git will detect as binary.
    (workdir / "logo.png").write_bytes(b"\x89PNG\r\n\x1a\n\x00")
    subprocess.run(["git", "add", "README.md", "logo.png"], cwd=workdir, check=True)
    subprocess.run(
        [
            "git",
            "-c",
            "user.email=a@b",
            "-c",
            "user.name=A",
            "commit",
            "-q",
            "-m",
            "feat: f22 fixture commit",
        ],
        cwd=workdir,
        check=True,
    )
    res = subprocess.run(
        ["git", "rev-parse", "HEAD"],
        cwd=workdir,
        capture_output=True,
        text=True,
        check=True,
    )
    return res.stdout.strip()


def _client_for_app(app: Any) -> httpx.AsyncClient:
    transport = httpx.ASGITransport(app=app)
    return httpx.AsyncClient(transport=transport, base_url="http://testserver")


def _wire_app_for_test(app: Any, *, workdir: Path) -> None:
    """Production wiring helper.  In Red phase F22 consumer integration tests
    run AFTER F23 has shipped wire_services — if it is still missing, the
    ImportError surfaces here, which is the correct failure mode."""
    from harness.api import wire_services  # type: ignore[attr-defined]

    wire_services(app, workdir=workdir)


