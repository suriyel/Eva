# F22-RT05 — POST /api/validate/feature-list.json returns ok=false + issues[]
# ---------------------------------------------------------------------------
@pytest.mark.real_http
async def test_f22_feature_22_rt05_post_validate_returns_issues(
    tmp_path: Path,
) -> None:
    """feature 22 RT05 FUNC/error + INTG/subprocess + FR-039: POST a known-bad
    feature-list.json (empty features array fails the validator script's
    minimum-feature check) → ok=false + issues[] with at least one entry.
    The FE binds CrossFieldErrorList to the issues array.
    """
    from harness.api import app

    workdir = tmp_path / "repo"
    workdir.mkdir()
    _git_init(workdir)
    _wire_app_for_test(app, workdir=workdir)
    bad_payload = {
        "path": "feature-list.json",
        "content": json.dumps({"features": []}),
    }
    async with _client_for_app(app) as client:
        resp = await client.post(
            "/api/validate/feature-list.json", json=bad_payload
        )

    assert (
        resp.status_code == 200
    ), f"feature 22 RT05 expected 200 (validator non-fatal failures); got {resp.status_code}: {resp.text!r}"
    body = resp.json()
    assert isinstance(body, dict), f"feature 22 RT05 expected dict; got {body!r}"
    assert (
        body.get("ok") is False
    ), f"feature 22 RT05 expected ok=False for empty feature-list.json; got {body!r}"
    issues = body.get("issues")
    assert (
        isinstance(issues, list) and len(issues) >= 1
    ), f"feature 22 RT05 expected non-empty issues[]; got {body!r}"


