# F22-RT01 — GET /api/settings/general exposes the FE-consumed surface
# ---------------------------------------------------------------------------
@pytest.mark.real_http
async def test_f22_feature_22_rt01_get_settings_general_exposes_fe_surface(
    tmp_path: Path,
) -> None:
    """feature 22 RT01 FUNC/happy: production app GET /api/settings/general
    must return a payload that the FE Zod schema can parse.  The FE consumes
    ``api_key_ref`` (object with service+user) OR ``api_key_masked`` (string
    starting with '***').  ``keyring_backend`` ∈ {'native','keyrings.alt','fail'}.

    Red expectation: production ``GeneralSettings`` model is currently
    ``extra='allow'`` with only ``ui_density``; missing the F22-required fields
    → assertion fails → SUT must add them in Green.
    """
    from harness.api import app

    workdir = tmp_path / "workdir"
    workdir.mkdir()
    _git_init(workdir)
    home = tmp_path / "home"
    home.mkdir()
    with _env_override(HARNESS_HOME=str(home)):
        _wire_app_for_test(app, workdir=workdir)
        async with _client_for_app(app) as client:
            resp = await client.get("/api/settings/general")
    assert (
        resp.status_code == 200
    ), f"feature 22 RT01 expected 200; got {resp.status_code}: {resp.text!r}"
    body = resp.json()
    assert isinstance(body, dict), f"feature 22 RT01 expected JSON object; got {body!r}"
    # FE consumes at least one of these surface fields; both must be string-or-null
    has_keyring_backend = "keyring_backend" in body
    has_api_key_masked = "api_key_masked" in body
    has_api_key_ref = "api_key_ref" in body
    assert (
        has_keyring_backend
    ), f"feature 22 RT01 production /api/settings/general missing 'keyring_backend' (Red expected — Green must add)"
    assert (
        has_api_key_masked or has_api_key_ref
    ), f"feature 22 RT01 production /api/settings/general missing both 'api_key_masked' and 'api_key_ref' (Red expected — Green must add)"
    # NFR-008: never expose plaintext in GET response
    flat = json.dumps(body)
    assert (
        "api_key_plaintext" not in flat
    ), f"feature 22 RT01 NFR-008 violation: GET response leaks plaintext field name; body={body!r}"


# ---------------------------------------------------------------------------
# F22-RT02 — PUT /api/settings/general round-trip preserves no plaintext
# ---------------------------------------------------------------------------
@pytest.mark.real_http
async def test_f22_feature_22_rt02_put_settings_general_no_plaintext_in_response(
    tmp_path: Path,
) -> None:
    """feature 22 RT02 FUNC/happy + NFR-008: PUT with api_key_plaintext must
    persist via keyring and return a payload free of plaintext.  config.json
    on disk must NOT contain plaintext (NFR-008 measurement).
    """
    from harness.api import app

    workdir = tmp_path / "workdir"
    workdir.mkdir()
    _git_init(workdir)
    home = tmp_path / "home"
    home.mkdir()
    plaintext = "sk-rt02-secret-aabb"
    with _env_override(HARNESS_HOME=str(home)):
        _wire_app_for_test(app, workdir=workdir)
        async with _client_for_app(app) as client:
            resp = await client.put(
                "/api/settings/general",
                json={"api_key_plaintext": plaintext, "ui_density": "comfortable"},
            )
        assert (
            resp.status_code == 200
        ), f"feature 22 RT02 expected 200; got {resp.status_code}: {resp.text!r}"
        body = resp.json()
        flat = json.dumps(body)
        assert (
            plaintext not in flat
        ), f"feature 22 RT02 NFR-008 violation: PUT response echoes plaintext; body={body!r}"
        # Disk must not carry plaintext either
        cfg = home / "config.json"
        if cfg.exists():
            on_disk = cfg.read_text(encoding="utf-8")
            assert (
                plaintext not in on_disk
            ), f"feature 22 RT02 NFR-008 violation: config.json contains plaintext"


