# ---------------------------------------------------------------------------
# F22-RT04 — GET /api/git/diff/:sha exposes binary-vs-text differentiation
# ---------------------------------------------------------------------------
@pytest.mark.real_http
async def test_f22_feature_22_rt04_get_git_diff_binary_marker(
    tmp_path: Path,
) -> None:
    """feature 22 RT04 BNDRY/binary-diff + IFR-005: real git repo with a
    binary file and a text file must yield a DiffPayload that flags the
    binary file via ``kind='binary'`` so the FE BinaryDiffPlaceholder branch
    can render without feeding empty hunks to react-diff-view.
    """
    from harness.api import app

    workdir = tmp_path / "repo"
    workdir.mkdir()
    sha = _git_commit_text_and_binary(workdir)
    _wire_app_for_test(app, workdir=workdir)
    async with _client_for_app(app) as client:
        resp = await client.get(f"/api/git/diff/{sha}")

    assert (
        resp.status_code == 200
    ), f"feature 22 RT04 expected 200; got {resp.status_code}: {resp.text!r}"
    body = resp.json()
    files = body.get("files") if isinstance(body, dict) else None
    assert (
        isinstance(files, list) and len(files) >= 2
    ), f"feature 22 RT04 expected ≥2 files in diff payload; got {body!r}"
    binary_files = [f for f in files if isinstance(f, dict) and f.get("kind") == "binary"]
    text_files = [f for f in files if isinstance(f, dict) and f.get("kind") == "text"]
    assert (
        len(binary_files) >= 1
    ), f"feature 22 RT04 expected ≥1 file with kind='binary'; got {files!r}"
    assert (
        len(text_files) >= 1
    ), f"feature 22 RT04 expected ≥1 file with kind='text'; got {files!r}"


