_KNOWN_OPTIONS = {
    "-h", "--help", "--path", "--lang",
    "--test-framework", "--coverage-tool",
    "--line-cov", "--branch-cov",
}


def _preflight_argv_guard(argv):
    """Inspect raw argv before argparse to surface a targeted diagnostic
    when a flag-shaped token is passed as the positional ``project_name``
    or as the value of ``--path``.

    Two separate scans:

    1. **Path value scan** — for ``--path <token>``, if ``token`` starts
       with ``-`` or is in :data:`_RESERVED_FLAGS`, refuse with an
       ``error: --path '...' looks like an argparse flag`` message.
    2. **Positional scan** — locate the first positional after stripping
       known options. If it begins with ``-`` or is in
       :data:`_RESERVED_FLAGS`, refuse with an ``error: project_name '...'
       looks like an argparse flag``. Special handling: when a single
       unknown flag-shaped token is the *only* positional candidate (e.g.,
       ``init_project.py --version``), treat it as the project_name to
       trigger the guard, since argparse would otherwise just print a
       generic ``missing required`` banner and the rogue ``--version/``
       directory risk remains plausible if a downstream caller wraps argv.

    Test Inventory B8-N1/N2/N3/P2 require stderr to contain "argparse
    flag" / "reserved" / "looks like" / "refuse".
    """
    args = list(argv)

    # 1) Path value scan
    for i, tok in enumerate(args):
        if tok == "--path" and i + 1 < len(args):
            nxt = args[i + 1]
            if nxt.startswith("-") or nxt in _RESERVED_FLAGS:
                sys.stderr.write(
                    f"error: --path '{nxt}' looks like an argparse flag; "
                    f"refuse to create directory\n"
                )
                sys.exit(2)

    # 2) Positional scan — find the first non-option token.
    idx = 0
    saw_double_dash = False
    positional_tok = None
    unknown_flag_tok = None
    while idx < len(args):
        tok = args[idx]
        if not saw_double_dash and tok == "--":
            saw_double_dash = True
            idx += 1
            continue
        if not saw_double_dash and tok.startswith("-"):
            if tok in _KNOWN_OPTIONS:
                value_taking = {
                    "--path", "--lang", "--test-framework",
                    "--coverage-tool", "--line-cov", "--branch-cov",
                }
                if tok in value_taking:
                    if idx + 1 < len(args):
                        idx += 2
                        continue
                    # Bare value-taking flag with no value supplied: the
                    # caller likely intended `--lang` as a project_name
                    # (B8-P2 reproducer). Surface guard message naming it
                    # as a reserved keyword.
                    unknown_flag_tok = tok
                    idx += 1
                    continue
                idx += 1
                continue
            # Unknown flag-shaped token — record it; argparse would later
            # reject, but we want our guard message first.
            unknown_flag_tok = tok
            idx += 1
            continue
        # First positional.
        positional_tok = tok
        break

    if positional_tok is None and unknown_flag_tok is not None:
        # Only an unknown flag was supplied — surface guard message
        # (test B8-N1/N2 reproducer: `init_project.py --version`).
        positional_tok = unknown_flag_tok

    if positional_tok is None:
        return  # let argparse handle "missing required"

    if positional_tok.startswith("-") or positional_tok in _RESERVED_FLAGS:
        kind = (
            "argparse flag" if positional_tok.startswith("-") and positional_tok not in _RESERVED_FLAGS
            else "reserved argparse keyword"
        )
        sys.stderr.write(
            f"error: project_name '{positional_tok}' looks like an {kind}; "
            f"refuse to create directory\n"
        )
        sys.exit(2)


def main():
    # B8 (feature #24) — pre-argparse guard. Without this, an external
    # plugin installer doing `init_project.py -- --version` produces a
    # rogue `--version/` directory in the repo root.
    _preflight_argv_guard(sys.argv[1:])

    parser = argparse.ArgumentParser(description="Initialize a long-task-agent project")
    parser.add_argument("project_name", help="Name of the project")
    parser.add_argument("--path", default=".", help="Output directory (default: current dir)")

    # Tech stack options
    parser.add_argument("--lang", default=None,
                        help="Project language (python/java/javascript/typescript/c/cpp). Auto-fills tool defaults.")
    parser.add_argument("--test-framework", default=None,
                        help="Test framework (e.g., pytest, junit, vitest, gtest)")
    parser.add_argument("--coverage-tool", default=None,
                        help="Coverage tool (e.g., pytest-cov, jacoco, c8, gcov)")

    # Quality gate thresholds
    parser.add_argument("--line-cov", type=int, default=90,
                        help="Min line coverage %% (default: 90)")
    parser.add_argument("--branch-cov", type=int, default=80,
                        help="Min branch coverage %% (default: 80)")

    args = parser.parse_args()

    # B8 (feature #24) — guard against external plugin installers passing
    # argparse flag names (e.g., "--version") or reserved keywords as
    # positional arguments. Reproducer: when an installer invokes
    # `python init_project.py -- --version`, argparse stops parsing at `--`
    # and accepts `--version` as `project_name`, which then becomes a
    # directory in the repo root (and gets collected by PyInstaller). The
    # guard rejects any value starting with `-` or matching a known reserved
    # CLI flag name.
    _validate_safe_arg(args.project_name, "project_name")
    _validate_safe_arg(args.path, "--path")

    out_dir = os.path.abspath(args.path)
    os.makedirs(out_dir, exist_ok=True)

    # Resolve tech stack from --lang preset, then override with explicit flags
    language = args.lang or "TODO"
    preset = LANG_PRESETS.get(language.lower(), {}) if language != "TODO" else {}
    test_framework = args.test_framework or preset.get("test_framework", "TODO")
    coverage_tool = args.coverage_tool or preset.get("coverage_tool", "TODO")

    # feature-list.json
    fl_path = os.path.join(out_dir, "feature-list.json")
    with open(fl_path, "w", encoding="utf-8") as f:
        json.dump(create_feature_list(
            args.project_name,
            language=language,
            test_framework=test_framework,
            coverage_tool=coverage_tool,
            line_coverage_min=args.line_cov,
            branch_coverage_min=args.branch_cov,
        ), f, indent=2, ensure_ascii=False)
    print(f"Created: {fl_path}")

    # CLAUDE.md (append reference, never overwrite)
    append_claude_md_reference(out_dir, args.project_name)

    # AGENTS.md (same reference for opencode, idempotent)
    append_agents_md_reference(out_dir, args.project_name)

    # task-progress.md
    tp_path = os.path.join(out_dir, "task-progress.md")
    with open(tp_path, "w", encoding="utf-8") as f:
        f.write(create_progress_log(args.project_name))
    print(f"Created: {tp_path}")

    # RELEASE_NOTES.md
    rn_path = os.path.join(out_dir, "RELEASE_NOTES.md")
    with open(rn_path, "w", encoding="utf-8") as f:
        f.write(create_release_notes(args.project_name))
    print(f"Created: {rn_path}")

    # scripts dir + copy helper scripts
    scripts_dir = os.path.join(out_dir, "scripts")
    os.makedirs(scripts_dir, exist_ok=True)

    # Resolve plugin root.
    # When this script runs from its original install location
    # (skills/long-task-init/scripts/), three levels up is the plugin root.
    # When the hook copies this script to a target project's scripts/ directory,
    # it also writes a `.long-task-plugin-root` hint file alongside it so the
    # copied script can locate the plugin's scripts/ directory correctly.
    _THIS_DIR = os.path.dirname(os.path.abspath(__file__))
    _hint_file = os.path.join(_THIS_DIR, '.long-task-plugin-root')
    if os.path.isfile(_hint_file):
        with open(_hint_file, encoding='utf-8') as _hf:
            _hint = _hf.read().strip()
        # Defensive: normalize Git Bash POSIX paths to Windows paths.
        # Git Bash writes /c/Users/... or c/Users/... (missing colon); Python on
        # Windows needs C:/Users/... for os.path.isdir() to succeed.
        if sys.platform == 'win32' and _hint:
            import re as _re
            _m = _re.match(r'^/?([a-zA-Z])/(.*)', _hint)
            if _m:
                _hint = _m.group(1).upper() + ':/' + _m.group(2)
        _PLUGIN_ROOT = _hint if (_hint and os.path.isdir(_hint)) else \
            os.path.dirname(os.path.dirname(os.path.dirname(_THIS_DIR)))
    else:
        _PLUGIN_ROOT = os.path.dirname(os.path.dirname(os.path.dirname(_THIS_DIR)))
    plugin_scripts_dir = os.path.join(_PLUGIN_ROOT, "scripts")

    helper_scripts = [
        "validate_features.py",
        "check_devtools.py",
        "validate_guide.py",
        "get_tool_commands.py",
        "validate_st_cases.py",
        "validate_increment_request.py",
        "validate_bugfix_request.py",
        "check_st_readiness.py",
        "check_real_tests.py",
        "check_ats_coverage.py",
        "feature_paths.py",
        "count_pending.py",
        "migrate_sub_status.py",
        "auto_loop.py",
        "auto_loop_opencode.py",
    ]
    for script_name in helper_scripts:
        src = os.path.join(plugin_scripts_dir, script_name)
        dst = os.path.join(scripts_dir, script_name)
        if os.path.exists(src) and os.path.abspath(src) != os.path.abspath(dst):
            shutil.copy2(src, dst)
            print(f"Copied: {script_name} -> {scripts_dir}")
        elif not os.path.exists(src):
            print(f"Warning: {script_name} not found in {plugin_scripts_dir}")

    # docs/plans dir
    plans_dir = os.path.join(out_dir, "docs", "plans")
    os.makedirs(plans_dir, exist_ok=True)

    # docs/rules dir
    rules_dir = os.path.join(out_dir, "docs", "rules")
    os.makedirs(rules_dir, exist_ok=True)
    print(f"Created: {rules_dir}")

    # docs/test-cases dir
    test_cases_dir = os.path.join(out_dir, "docs", "test-cases")
    os.makedirs(test_cases_dir, exist_ok=True)
    print(f"Created: {test_cases_dir}")

    # docs/report dir
    report_dir = os.path.join(out_dir, "docs", "report")
    os.makedirs(report_dir, exist_ok=True)
    print(f"Created: {report_dir}")

    # docs/templates dir — copy ST case template
    templates_dir = os.path.join(out_dir, "docs", "templates")
    os.makedirs(templates_dir, exist_ok=True)
    plugin_templates_dir = os.path.join(_PLUGIN_ROOT, "docs", "templates")
    st_template_src = os.path.join(plugin_templates_dir, "st-case-template.md")
    st_template_dst = os.path.join(templates_dir, "st-case-template.md")
    if os.path.exists(st_template_src) and os.path.abspath(st_template_src) != os.path.abspath(st_template_dst):
        shutil.copy2(st_template_src, st_template_dst)
        print(f"Copied: st-case-template.md -> {templates_dir}")

    # examples dir + README.md
    examples_dir = os.path.join(out_dir, "examples")
    os.makedirs(examples_dir, exist_ok=True)
    examples_readme = os.path.join(examples_dir, "README.md")
    with open(examples_readme, "w", encoding="utf-8") as f:
        f.write(create_examples_readme(args.project_name))
    print(f"Created: {examples_readme}")

    print(f"\nProject '{args.project_name}' initialized at {out_dir}")
    print("Created: feature-list.json, CLAUDE.md, AGENTS.md, task-progress.md, RELEASE_NOTES.md, examples/, scripts/ (with helper scripts), docs/plans/, docs/test-cases/, docs/report/, docs/templates/")
    print("TODO (LLM generates during Initializer phase):")
    print("  - long-task-guide.md         (tailored Worker guide from SKILL.md + references + design doc)")
    print("  - init.sh / init.ps1         (environment bootstrap from design doc tech stack)")


if __name__ == "__main__":
    main()
