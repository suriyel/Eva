#!/usr/bin/env python3
"""
Initialize a long-task-agent project structure.

Creates the deterministic scaffold artifacts needed for multi-session agent work:
- feature-list.json (empty template with tech_stack, quality_gates, required_configs)
- task-progress.md (empty progress log)
- RELEASE_NOTES.md (living release notes, Keep a Changelog format)
- CLAUDE.md (appended with long-task-agent reference for cross-session continuity)
- examples/ directory with README.md (runnable examples for completed features)
- scripts/ directory (for validate_features.py, check_configs.py, check_devtools.py)
- docs/plans/ directory (for design docs and implementation plans)

NOTE: The following files are NOT generated by this script — they are created by the
LLM during the Initializer phase, tailored to the project's specific tech stack:
- long-task-guide.md (Worker session guide — LLM generates from SKILL.md + references)
- init.sh / init.ps1 (environment bootstrap — LLM generates from design doc)

Feature schema supports optional UI fields:
- "ui": true/false — marks features that require Chrome DevTools MCP functional testing
- "ui_entry": "/path" — entry URL for the UI feature (used by Worker for navigate_page)
- UI features must have at least one verification_step prefixed with "[devtools]"

Usage:
    python scripts/init_project.py <project-name> [--path <output-dir>]
           [--lang <language>] [--test-framework <framework>]
           [--coverage-tool <tool>]
           [--line-cov <0-100>] [--branch-cov <0-100>]
"""

import argparse
